package com.xalap.uaa.service.audit.security;

import com.xalap.uaa.service.authority.SecurityConstants;
import com.xalap.uaa.service.utils.SecurityUtils;
import org.springframework.data.domain.AuditorAware;
import org.springframework.stereotype.Component;

import java.util.Optional;

/**
 * Implementation of {@link AuditorAware} based on Spring Security.
 */
@Component
public class SpringSecurityAuditorAware implements AuditorAware<String> {

    @Override
    public Optional<String> getCurrentAuditor() {
        return Optional.of(SecurityUtils.getCurrentUserLogin().orElse(SecurityConstants.SYSTEM_ACCOUNT));
    }
}
